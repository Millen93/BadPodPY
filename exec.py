import json
import subprocess


def exec(node, image, ns):
    pod_manifest = {
        'apiVersion': 'v1',
        'kind': 'Pod',
        'metadata': {
            'name': 'nothing-interesting-here'
        },
        'spec': {
            'tolerations': [
              {
                'key': 'node-role.kubernetes.io/master',
		'operator': 'Exists',
		'effect': 'NoSchedule'
	      }
            ],
            'nodeSelector': {
                'kubernetes.io/hostname': node
            },
            'hostPID': True,
            'hostNetwork': True,
            'containers': [{
                'image': image,
                'name': 'exec',
                "command": [
                    "/nsenter", "--all", "--target=1", "--", "su", "-"
                ],
                "stdin": True,
                "tty": True,
                "securityContext": {
                    "privileged": True
                },
            }]
        }
    }
    overrides = json.dumps(pod_manifest)
    subprocess.run(["kubectl", "run", "-n", ns, 'nothing-interesting-here', "--image=override", "--restart=Never", "-it", "--rm", "--overrides",
                    overrides])

